def pregunta_07():
    """
    Retorne una lista de tuplas que asocien las columnas 0 y 1. Cada tupla
    contiene un valor posible de la columna 2 y una lista con todas las letras
    asociadas (columna 1) a dicho valor de la columna 2.

    Rta/
    [(0, ['C']),
     (1, ['E', 'B', 'E']),
     (2, ['A', 'E']),
     (3, ['A', 'B', 'D', 'E', 'E', 'D']),
     (4, ['E', 'B']),
     (5, ['B', 'C', 'D', 'D', 'E', 'E', 'E']),
     (6, ['C', 'E', 'A', 'B']),
     (7, ['A', 'C', 'E', 'D']),
     (8, ['E', 'D', 'E', 'A', 'B']),
     (9, ['A', 'B', 'E', 'A', 'A', 'C'])]

    """
    nombre_del_archivo = "files/input/data.csv"
    
    letras_por_valor = {}
    
    archivo = open(nombre_del_archivo, "r")
    
    for linea in archivo:
        linea_limpia = linea.strip()
        
   
            
        partes = linea_limpia.split('\t')
        
        letra_col0 = partes[0]
        
        valor_col2_str = partes[1]
        valor_col2_num = int(valor_col2_str)
        
        if valor_col2_num not in letras_por_valor:
            letras_por_valor[valor_col2_num] = []
        
        letras_por_valor[valor_col2_num].append(letra_col0)
            
    archivo.close()
    
    lista_de_tuplas = list(letras_por_valor.items())
    
    resultado_ordenado = sorted(lista_de_tuplas)
      
    return resultado_ordenado
